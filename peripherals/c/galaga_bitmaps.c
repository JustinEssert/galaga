		case 3:
			if(angle)
				lcd_draw_unit(x, 24, y, 24, galaga_up_left_m0, galaga_up_left_m1, GALAGA_COLOR_1, GALAGA_COLOR_2, GALAGA_COLOR_3, LCD_COLOR_BLACK, flipX, flipY);
